package looping;

public class StringLoop {

	public static void main(String[] args) {
				
			String name = ("Angel, Zion, Charles, Holly, Jacob, Mike, Paul, Cassie, ");
			int i = name.length();
			for(i = 0; i<name.length(); i++) {
				
				System.out.println("Kids name is: " + name.charAt(i));
			}
		
	
	}

}
